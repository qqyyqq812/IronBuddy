#!/bin/bash
# -----------------------------------------------------------------------------
# 脚本：build_env.sh (一键组建 RK3399Pro 古早模型炼丹炉)
# 说明：此脚本会读取同目录下的 Dockerfile，在开发机本地打出独立隔离环境镜像。
# -----------------------------------------------------------------------------

set -e

DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
IMAGE_NAME="ironbuddy_rknn_compiler_v1"

echo "=========================================================="
echo "🛡️  开始铸造 RKNN-Toolkit 1.7 强制隔离系统容器 (Ubuntu 18.04)"
echo "=========================================================="
echo "[步骤 1] 正在通过 Docker Daemon 拉取并在后台编译底层镜像，请耐心等待..."

cd "$DIR"
docker build -t "$IMAGE_NAME" .

echo ""
echo "✅ 容器环境 [ $IMAGE_NAME ] 打包成功！"
echo ""
echo "[下一步指令] 请总经理在需要时，复制下方安全挂载命令以切入隔离车间终端："
echo ""
echo "docker run -it --rm -v \$(pwd)/../../:/workspace $IMAGE_NAME /bin/bash"
echo ""
echo "=========================================================="
